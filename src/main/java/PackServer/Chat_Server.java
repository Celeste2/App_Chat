    @Override
    public void run() {
        String nombre,ip,msj;
        try {
            sSocket= new ServerSocket(1306);
            logger.info("Abriendo al puerto 1306");
            Cliente recibido;
            while(true){
                cSocket=sSocket.accept();
                entrada=new ObjectInputStream(cSocket.getInputStream());
                recibido=(Cliente) entrada.readObject();
                nombre=recibido.getNombre();
                ip=recibido.getIp();
                msj=recibido.getMensaje();
                logger.info("Viendo la ip".concat(ip));
                logger.info("Viendo el nombre: ".concat(nombre));
                //txtServer.append("\n"+recibido.getNombre()+" : "+recibido.getMensaje()+"\n");
                txtServer.append("Cliente: "+nombre+ "   IP: "+ip+"\n"+"Mensaje: "+msj+"\n");
                Socket enviar= new Socket(ip,2020);
                ObjectOutputStream salida= new ObjectOutputStream(enviar.getOutputStream());
                salida.writeObject(recibido);
               cSocket.close();
            }
        } catch (IOException ex) {
            System.out.println(ex.getMessage());
        } catch (ClassNotFoundException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
    private void close() throws IOException{
        logger.info("Se supone que estoy cerrando");
        entrada.close();
        cSocket.close();
        sSocket.close();
    }
